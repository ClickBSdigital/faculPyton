# Verifique se três números estão em ordem crescente
v1 = int(input("Digite o primeiro número: "))
v2 = int(input("Digite o segundo número: "))
v3 = int(input("Digite o terceiro número: "))

if v1 < v2 < v3:
    print("Os números estão em ordem crescente.")
else:
    print("Os números não estão em ordem crescente.")
